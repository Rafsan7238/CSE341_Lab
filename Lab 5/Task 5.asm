CODE SEGMENT
	START:

	MOV AX, DATA
	MOV DS, AX
	MOV ES, AX

	MOV AH, 2
	MOV CL, 0	    ; TO KEEP COUNT OF CHARS PER LINE
	MOV DH, 080H

	PRINT:

	MOV DL, DH
	INT 21H

	CMP DL, 0FFH
	JG EXIT

	INC DL
	MOV DH, DL
	MOV DL, 020H	; SPACE
	INT 21H
	
	INC CL
	CMP CL, 10
	JE NEW_LINE
	JMP PRINT

	NEW_LINE:
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	MOV CL, 0	    ; RESET COUNTER
	JMP PRINT

	EXIT:
	MOV AX, 4C00H
	INT 21H

ENDS 
END START