DATA SEGMENT
	
	A DB 5 DUP(?)

ENDS

CODE SEGMENT
	
	START:
	
	MOV AX, DATA
	MOV DS, AX
	MOV ES, AX

	; TAKE INPUT

	MOV CX, 5
	MOV SI, 0
	MOV AH, 1

	INPUT:
	
	INT 21H
	MOV A[SI], AL
	ADD SI, 1
	LOOP INPUT

	; SORT

	MOV CX, 5
	MOV DX, CX
	
	OUTER_LOOP:
	
	MOV SI, 0
	MOV AX, SI
	INC AX
	MOV DI, AX
	MOV DX, CX 
	
	DEC CX
	CMP CX, 0
	JL EXIT_OUTER_LOOP
	
	INNER_LOOP: 
	
	MOV AL, A[SI]
	CMP AL, A[DI]
	JG IF_LOOP  
	
	ELSE_LOOP:
	
	ADD SI, 1
	ADD DI, 1
	DEC DX
	CMP DX, 1
	JLE OUTER_LOOP
	JG INNER_LOOP
	
	IF_LOOP: 
	
	MOV AL, A[SI]
	MOV AH, A[DI]
	MOV A[SI], AH
	MOV A[DI], AL
	
	ADD SI, 1
	ADD DI, 1
	DEC DX
	CMP DX, 1
	JLE OUTER_LOOP
	JG INNER_LOOP
	
	; PRINT ARRAY 
	
	EXIT_OUTER_LOOP:
	
	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	
	MOV CX, 5
	MOV SI, 0
	
	PRINT:
	
	MOV DL, A[SI] 
	INT 21H
	MOV DL, ' '
	INT 21H
	ADD SI, 1
	LOOP PRINT
	
	EXIT:
	
	MOV AX, 4C00H
	INT 21H
	
ENDS
END START
	