CODE SEGMENT

	START:

	MOV AX, DATA
	MOV DS, AX
	MOV ES, AX

	; TAKE INPUT 

	MOV CX, 3
	MOV AH, 1
	LEA SI, A

	INPUT:
	
	INT 21H 
	SUB AL, 30H
	MOV [SI], AL
	ADD SI, 1
	LOOP INPUT

	; FIND MAX

	MOV CX, 2
	MOV SI, 0
	MOV AL, A[SI]
	MOV MAX, AL
	ADD SI, 1

	FIND:
    
    MOV AL, A[SI]
	CMP AL, MAX
	JG CHANGE_MAX
	JLE CONTINUE

	CHANGE_MAX:
    
	MOV MAX, AL
	
	CONTINUE:

	ADD SI, 1
	LOOP FIND 
	
	; PRINT MAX
	
	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	
	ADD MAX, 30H
	MOV DL, MAX
	INT 21H

	EXIT:
	MOV AX, 4C00H
	INT 21H

ENDS
END START