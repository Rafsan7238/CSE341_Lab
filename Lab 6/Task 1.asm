CODE SEGMENT 
	START:

	MOV AX, DATA
	MOV DS, AX
	MOV ES, AX

	; INPUT LENGTH

	MOV AH, 1
	INT 21H
	SUB AL, 30H
	MOV LEN, AL

	; INPUT NAME 
	
    MOV CH, 0
	MOV CL, LEN
	MOV SI, 0
	
	READ:

	INT 21H
	MOV N[SI], AL
	ADD SI, 1
	LOOP READ

	; PRINT NAME

	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	
    MOV CH, 0
	MOV CL, LEN
	MOV SI, 0
	
	PRINT:
	MOV DL, N[SI]
	INT 21H 
	ADD SI, 1
	LOOP PRINT

	EXIT:
	MOV AX, 4C00H
	INT 21H

ENDS
END START
	
	